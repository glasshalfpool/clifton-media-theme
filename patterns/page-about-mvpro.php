<h1 class="wp-block-heading has-text-align-center">About Us</h1>
<!-- /wp:heading -->

<!-- wp:group {"align":"wide","layout":{"type":"default"}} -->
<div class="wp-block-group alignwide"><!-- wp:paragraph -->
<p>The MVPro Media team is a melting pot of talent working together to deliver the news you need to know about. Coming from diverse professional backgrounds and experiences connected to machine vision and industrial image processing, we bring our readers committed and unique content they won’t find elsewhere.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>MVPro Media exists under the umbrella of Clifton Media Lab, a publishing company that was created by founders Alex Sullivan and Paul Wilson in 2013. Clifton Media Lab also owns and operates the financial B2B publications <a href="https://ifamagazine.com/" target="_blank" rel="noreferrer noopener">IFA Magazine</a>, <a href="https://ifamagazine.com/gbi-great-british-investments/" target="_blank" rel="noreferrer noopener">GBI Magazine</a> and <a href="https://wealthdfm.com/" target="_blank" rel="noreferrer noopener">Wealth DFM</a>.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Alex previously worked in the machine vision industry as a commercial and marketing director, creating the magazine IMVE Europe. Following the foundation of Clifton Media Lab and its financial publications, Alex launched MVPro Media.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Our mission at MVPro Media as a B2B publication is to find and report on the global events and companies that keep Machine Vision moving forward. UK-based, we cover global news but with a European flavouring. We get access to the most amazing advancements in machine vision and image processing technologies before anyone else; we travel to incredible locations to report on stories, see the impact it has and listen to forward-thinking ideas.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>We produce daily news articles on the website, and a weekly Friday newsletter featuring a round-up of the week’s news. We also publish a bi-monthly magazine, featuring exclusive interviews with the biggest and best names in the industry, along with stories you cannot read about anywhere else.</p>
