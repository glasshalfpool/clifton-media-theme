<h2 class="wp-block-heading has-text-align-left">Style and Substance</h2>
<!-- /wp:heading -->

<!-- wp:paragraph {"align":"left"} -->
<p class="has-text-align-left">Thanks to its trademark blend of sharp professional analysis and strongly argued opinions from leading industry figures, IFA Magazine simply exudes quality – all wrapped up in one of the most visually exciting packages you’ll find anywhere in the publishing environment.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"align":"left"} -->
<p class="has-text-align-left">Published ten times each year, from the high-gloss cover to the startlingly intense quality of its graphic imagery, the design of IFA Magazine owes more to the higher-level lifestyle magazines than to the traditional financial press. Our readers know how to appreciate the finer points of a well-presented publication, and they are proving intensely loyal.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"align":"left"} -->
<p class="has-text-align-left">It’s not only the specialist sections which include coverage of technical issues, better business practice and our regular look behind the scenes at today’s successful advisory firms that readers love. It’s also the unmistakable twist of humour that keeps them turning the pages.</p>
