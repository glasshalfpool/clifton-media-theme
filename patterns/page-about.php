<h1 class="wp-block-heading has-text-align-center">About Us</h1>
<!-- /wp:heading -->

<!-- wp:paragraph {"align":"center"} -->
<p class="has-text-align-center">First published back in 2010, IFA Magazine delivers news, analysis and thought leadership for professional financial advisers, planners, paraplanners and wealth managers. We’re proud to be a part of Clifton Media Lab, an independent publishing company that is based in the Clifton area of Bristol.</p>
<!-- /wp:paragraph -->

<!-- wp:spacer {"height":"var:preset|spacing|medium"} -->
<div style="height:var(--wp--preset--spacing--medium)" aria-hidden="true" class="wp-block-spacer"></div>
