<h2 class="wp-block-heading">Strong focus on readership</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>We don’t lose sight of the fact that our readers are well-qualified investment professionals looking for something a bit different from the publications they read.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>We use our vast experience in the sector to establish what topics and subjects readers will find interesting – as well as what they won’t. Clearly, we try to avoid those elements at all costs. Because we understand that our readers are professionals, we know that they have sophisticated needs when it comes to what they want to read. They like to read content which is on their wavelength, is pitched at the right level to stimulate their thinking as well as to inform.</p>
